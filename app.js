function conferirAbacate() {
    const respostaAcabate = document.getElementById("respostaAcabate").value;

    if (respostaAcabate == "a-ba-ca-te") {
        document.getElementById("respAbacate").innerHTML = "Parabéns!";
    }
    else {
        document.getElementById("respAbacate").innerHTML = "Resposta Errada! Tente novamente";
    }
}
